#!/bin/bash

# สคริปต์ทดสอบฟังก์ชันนับจำนวนคำภาษาไทย
# สำหรับใช้ตรวจสอบความแม่นยำของฟังก์ชัน count_thai_words

set -e

# โหลดฟังก์ชันสากล (Common Functions)
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
source "$SCRIPT_DIR/common.sh"

# การแสดงผลแถบสีต่างๆ
GREEN='\033[0;32m'
RED='\033[0;31m'
YELLOW='\033[1;33m'
NC='\033[0m' # No Color (ล้างค่าสี)

echo "========================================"
echo "การทดสอบฟังก์ชันนับจำนวนคำภาษาไทย"
echo "========================================"
echo ""

# สร้างไดเรกทอรีชั่วคราวสำหรับรันทดสอบ
TEST_DIR=$(mktemp -d)
trap "rm -rf $TEST_DIR" EXIT

# กรณีทดสอบที่ 1: ข้อความภาษาไทยล้วน
echo "## การทดสอบที่ 1: ข้อความภาษาไทยล้วน"
cat > "$TEST_DIR/test1.md" << 'EOF'
วันนี้อากาศดีมาก ฉันไปเดินเล่นที่สวนสาธารณะ
เห็นผู้คนมากมายกำลังออกกำลังกาย
EOF
# คำที่ควรตัดได้: วันนี้ / อากาศ / ดี / มาก / ฉัน / ไป / เดินเล่น / ที่ / สวนสาธารณะ / เห็น / ผู้คน / มากมาย / กำลัง / ออกกำลังกาย
expected1=14
actual1=$(count_thai_words "$TEST_DIR/test1.md")
echo "  จำนวนคำที่คาดหวัง (โดยประมาณ): $expected1"
echo "  จำนวนคำที่นับได้จริง: $actual1"
if [ "$actual1" -ge $((expected1 - 2)) ] && [ "$actual1" -le $((expected1 + 2)) ]; then
    echo -e "  ${GREEN}✅ การทดสอบผ่าน (อยู่ในเกณฑ์ยอมรับได้)${NC}"
else
    echo -e "  ${RED}❌ การทดสอบล้มเหลว${NC}"
fi
echo ""

# กรณีทดสอบที่ 2: ข้อความที่มีสัญลักษณ์ Markdown
echo "## การทดสอบที่ 2: ข้อความที่มีสัญลักษณ์ Markdown"
cat > "$TEST_DIR/test2.md" << 'EOF'
# บทที่หนึ่ง

นี่คือเนื้อหาที่**สำคัญ**มาก

- รายการที่หนึ่ง
- รายการที่สอง

> นี่คือข้อความอ้างอิง
EOF
# คำที่ควรนับได้: บท / ที่ / หนึ่ง / นี่ / คือ / เนื้อหา / ที่ / สำคัญ / มาก / รายการ / ที่ / หนึ่ง / รายการ / ที่ / สอง / นี่ / คือ / ข้อความ / อ้างอิง
expected2=19
actual2=$(count_thai_words "$TEST_DIR/test2.md")
echo "  จำนวนคำที่คาดหวัง (โดยประมาณ): $expected2"
echo "  จำนวนคำที่นับได้จริง: $actual2"
if [ "$actual2" -ge $((expected2 - 3)) ] && [ "$actual2" -le $((expected2 + 3)) ]; then
    echo -e "  ${GREEN}✅ การทดสอบผ่าน (Success)${NC}"
else
    echo -e "  ${YELLOW}⚠️ พบความต่างของจำนวนคำ: $((actual2 - expected2))${NC}"
fi
echo ""

# กรณีทดสอบที่ 3: ข้อความภาษาไทยผสมภาษาอังกฤษและตัวเลข
echo "## การทดสอบที่ 3: ข้อความแบบผสมภาษาไทยและภาษาอังกฤษ"
cat > "$TEST_DIR/test3.md" << 'EOF'
นี่คือไฟล์ทดสอบ test ตัวอย่าง
ประกอบด้วยตัวเลข 123 และคำ English
EOF
# กลุ่มคำที่ควรถูกนับแยก: นี่ / คือ / ไฟล์ / ทดสอบ / test / ตัวอย่าง / ประกอบด้วย / ตัวเลข / 123 / และ / คำ / English
expected3=12
actual3=$(count_thai_words "$TEST_DIR/test3.md")
echo "  จำนวนคำที่คาดหวัง: ประมาณ $expected3"
echo "  จำนวนคำที่นับได้จริง: $actual3"
if [ "$actual3" -ge $((expected3 - 2)) ] && [ "$actual3" -le $((expected3 + 2)) ]; then
    echo -e "  ${GREEN}✅ การทดสอบผ่าน (อยู่ในเกณฑ์ที่ยอมรับได้)${NC}"
else
    echo -e "  ${YELLOW}⚠️ จำนวนคำมีความคลาดเคลื่อนสูง${NC}"
fi
echo ""

# กรณีทดสอบที่ 4: ข้อความที่มีบล็อกโค้ดโปรแกรม (Code Blocks)
echo "## การทดสอบที่ 4: ข้อความที่มีบล็อกโค้ดโปรแกรม"
cat > "$TEST_DIR/test4.md" << 'EOF'
นี่คือข้อความปกติ

```javascript
console.log("ข้อความในนี้ต้องไม่ถูกนับรวม");
